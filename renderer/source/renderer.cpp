void Renderer::pickPhysicalDevice() {
    uint32_t deviceCount = 0;
    vkEnumeratePhysicalDevices(instance, &deviceCount, nullptr);

    if(deviceCount == 0) {
        rendererLogger->error("Failed to find a GPU with Vulkan support!");
        throw std::runtime_error("Failed to find a GPU with Vulkan support!");
    }

    std::vector<VkPhysicalDevice> devices(deviceCount);
    vkEnumeratePhysicalDevices(instance, &deviceCount, devices.data());

    std::multimap<int, VkPhysicalDevice> candidates;

    for(auto const &device : devices) {
        int score = rateDeviceSuitability(device);
        candidates.insert(std::make_pair(score, device));
    }

    // TODO: rework this
    for(auto iter = candidates.rbegin(); iter not_eq candidates.rend(); ++iter) {
        if (iter->first > 0 and isDeviceSuitable(iter->second)) {
            physicalDevice = iter->second;
            break;
        }
    }

    if(physicalDevice == VK_NULL_HANDLE) {
        rendererLogger->error("Failed to find a suitable GPU");
        throw std::runtime_error("Failed to find a suitable GPU!");
    } else {
        vkGetPhysicalDeviceProperties(physicalDevice, &deviceProperties);
        rendererLogger->debug("Using device: {}", deviceProperties.deviceName);
    }
}

bool Renderer::isDeviceSuitable(VkPhysicalDevice device) {
    QueueFamilyIndices indices = findQueueFamilies(device);

    bool extensionsSupported = checkDeviceExtensionSupport(device);

    bool swapChainAdequate = false;
    if(extensionsSupported) {
        SwapChainSupportDetails swapChainSupport = querySwapChainSupport(device);
        swapChainAdequate = not swapChainSupport.formats.empty() and not swapChainSupport.presentModes.empty();
    }

    VkPhysicalDeviceFeatures supportedFeatures;
    vkGetPhysicalDeviceFeatures(device, &supportedFeatures);

    return indices.isComplete() and extensionsSupported and swapChainAdequate and supportedFeatures.samplerAnisotropy;
}

int Renderer::rateDeviceSuitability(VkPhysicalDevice device) {
    VkPhysicalDeviceProperties devProp = {};
    VkPhysicalDeviceFeatures devFeat = {};

    vkGetPhysicalDeviceFeatures(device, &devFeat);
    vkGetPhysicalDeviceProperties(device, &devProp);

    rendererLogger->debug("Found device: {}", devProp.deviceName);  

    int score = 0;

    if (devProp.deviceType == VK_PHYSICAL_DEVICE_TYPE_DISCRETE_GPU) {
        score += 1000;
    }

    score += devProp.limits.maxImageDimension2D;

    if(not devFeat.geometryShader) {
        rendererLogger->debug("Device {} doesn't support geometry shaders", devProp.deviceName);
        return 0;
    }

    return score;
}

QueueFamilyIndices Renderer::findQueueFamilies(VkPhysicalDevice device) {
    QueueFamilyIndices indices;

    uint32_t queueFamilyCount = 0;
    vkGetPhysicalDeviceQueueFamilyProperties(device, &queueFamilyCount, nullptr);

    std::vector<VkQueueFamilyProperties> queueFamilies(queueFamilyCount);
    vkGetPhysicalDeviceQueueFamilyProperties(device, &queueFamilyCount, queueFamilies.data());

    int i = 0;
    for(auto const &queueFamily : queueFamilies) {
        if (queueFamily.queueCount > 0 and queueFamily.queueFlags bitand VK_QUEUE_GRAPHICS_BIT) {
            indices.graphicsFamily = i;
        }

        VkBool32 presentSupport = false;
        vkGetPhysicalDeviceSurfaceSupportKHR(device, i, surface, &presentSupport);

        if(queueFamily.queueCount > 0 and presentSupport) {
            indices.presentFamily = i;
        }

        if(indices.isComplete()) {
            break;
        }

        ++i;
    }
    return indices;
}

SwapChainSupportDetails Renderer::querySwapChainSupport(VkPhysicalDevice device) {
    SwapChainSupportDetails details;
    vkGetPhysicalDeviceSurfaceCapabilitiesKHR(device, surface, &details.capabilities);

    uint32_t formatCount;
    vkGetPhysicalDeviceSurfaceFormatsKHR(device, surface, &formatCount, nullptr);

    if(formatCount not_eq 0) {
        details.formats.resize(formatCount);
        vkGetPhysicalDeviceSurfaceFormatsKHR(device, surface, &formatCount, details.formats.data());
    }

    uint32_t presentModeCount;
    vkGetPhysicalDeviceSurfacePresentModesKHR(device, surface, &presentModeCount, nullptr);

    if(presentModeCount not_eq 0) {
        details.presentModes.resize(presentModeCount);
        vkGetPhysicalDeviceSurfacePresentModesKHR(device, surface, &presentModeCount, details.presentModes.data());
    }

    return details;
}

VkSurfaceFormatKHR Renderer::chooseSwapSurfaceFormat(std::vector<VkSurfaceFormatKHR> const &availableFormats) {
    for(auto const &availableFormat : availableFormats) {
        if(availableFormat.format == VK_FORMAT_B8G8R8A8_UNORM and availableFormat.colorSpace == VK_COLOR_SPACE_SRGB_NONLINEAR_KHR) {
            return availableFormat;
        }
    }

    return availableFormats[0];
}

VkPresentModeKHR Renderer::chooseSwapPresentMode(std::vector<VkPresentModeKHR> const &availablePresentModes) {
    for(auto const &availablePresentMode : availablePresentModes) {
        if(availablePresentMode == VK_PRESENT_MODE_IMMEDIATE_KHR) {
            rendererLogger->debug("Using present mode: VK_PRESENT_MODE_IMMEDIATE_KHR");
            return availablePresentMode;
        }
    }

    rendererLogger->debug("Using present mode: VK_PRESENT_MODE_FIFO_KHR");
    return VK_PRESENT_MODE_FIFO_KHR;
}

VkExtent2D Renderer::chooseSwapExtent(VkSurfaceCapabilitiesKHR const &capabilities) {
    if (capabilities.currentExtent.width not_eq UINT32_MAX) {
        return capabilities.currentExtent;
    } else {

        VkExtent2D actualExtent = {
            static_cast<uint32_t>(frameBufferWidth),
            static_cast<uint32_t>(frameBufferHeight)
        };

        actualExtent.width = std::max(capabilities.minImageExtent.width, std::min(capabilities.maxImageExtent.width, actualExtent.width));
        actualExtent.height = std::max(capabilities.minImageExtent.height, std::min(capabilities.maxImageExtent.height, actualExtent.height));

        return actualExtent;
    }
}

bool Renderer::checkDeviceExtensionSupport(VkPhysicalDevice device) {
    uint32_t extensionCount;
    vkEnumerateDeviceExtensionProperties(device, nullptr, &extensionCount, nullptr);

    std::vector<VkExtensionProperties> availableExtensions(extensionCount);
    vkEnumerateDeviceExtensionProperties(device, nullptr, &extensionCount, availableExtensions.data());

    for(auto extension : availableExtensions) {
        rendererLogger->debug("Found device extension: {}", extension.extensionName);
    }

    for(auto i = 0; i < optionalExtensions.size(); i++) {
        bool extensionFound = false;

        for(auto const &extension : availableExtensions) {
            if(std::strcmp(optionalExtensions.at(i).c_str(), extension.extensionName) == 0) {
                extensionFound = true;
                rendererLogger->debug("Added optional device extension: {}", extension.extensionName);
                deviceExtensions.push_back(extension.extensionName);
                break;
            }
        }

        if(not extensionFound) {
            rendererLogger->warn("Couldn't add optional device extension: {}", optionalExtensions.at(i));
        }
    }

    for(auto const *wantedExtension : deviceExtensions) {
        bool extensionFound = false;

        for(auto const extension : availableExtensions) {
            if (std::strcmp(extension.extensionName, wantedExtension) == 0) {
                extensionFound = true;
                break;
            }
        }

        if(not extensionFound) {
            rendererLogger->error("Couldn't find device extension {}", wantedExtension);
            return false;
        }
    }

    return true;
}

void Renderer::createInstance() {
    if(enableValidationLayers and not checkValidationLayerSupport()) {
        rendererLogger->error("Validation layers requested, but not available");
        throw std::runtime_error("Validation layers requested, but not available!");
    }

    VkApplicationInfo appInfo = {};
    appInfo.sType = VK_STRUCTURE_TYPE_APPLICATION_INFO;
    appInfo.pApplicationName = title.c_str();
    appInfo.applicationVersion = VK_MAKE_VERSION(1, 0, 0);
    appInfo.pEngineName = engineName.c_str();
    appInfo.engineVersion = VK_MAKE_VERSION(1, 0, 0);
    appInfo.apiVersion = VK_API_VERSION_1_0;

    VkInstanceCreateInfo createInfo = {};
    createInfo.sType = VK_STRUCTURE_TYPE_INSTANCE_CREATE_INFO;
    createInfo.pApplicationInfo = &appInfo;

    auto extensions = getRequiredExtensions();
    createInfo.enabledExtensionCount = static_cast<uint32_t>(extensions.size());
    createInfo.ppEnabledExtensionNames = extensions.data();

    VkDebugUtilsMessengerCreateInfoEXT debugCreateInfo;
    if(enableValidationLayers) {
        createInfo.enabledLayerCount = static_cast<uint32_t>(validationLayers.size());
        createInfo.ppEnabledLayerNames= validationLayers.data();

        populateDebugMessengerCreateInfo(debugCreateInfo);
        createInfo.pNext = (VkDebugUtilsMessengerCreateInfoEXT*) &debugCreateInfo;
    } else {
        createInfo.enabledLayerCount = 0;
        createInfo.pNext = nullptr;
    }

    if (vkCreateInstance(&createInfo, nullptr, &instance) not_eq VK_SUCCESS) {
        rendererLogger->error("Failed to create Vulkan instance");
        throw std::runtime_error("Failed to create Vulkan instance!");
    }
}

void Renderer::createLogicalDevice() {
    QueueFamilyIndices indices = findQueueFamilies(physicalDevice);

    std::vector<VkDeviceQueueCreateInfo> queueCreateInfos;
    std::set<uint32_t> uniqueQueueFamilies = {indices.graphicsFamily.value(), indices.presentFamily.value()};

    float queuePriority = 1.0f;
    for(uint32_t queueFamily : uniqueQueueFamilies) {
        VkDeviceQueueCreateInfo queueCreateInfo = {};
        queueCreateInfo.sType = VK_STRUCTURE_TYPE_DEVICE_QUEUE_CREATE_INFO;
        queueCreateInfo.queueFamilyIndex = queueFamily;
        queueCreateInfo.queueCount = 1;
        queueCreateInfo.pQueuePriorities = &queuePriority;
        queueCreateInfos.push_back(queueCreateInfo);
    }

    VkPhysicalDeviceFeatures deviceFeatures = {};
    deviceFeatures.samplerAnisotropy = VK_TRUE;

    VkDeviceCreateInfo createInfo = {};
    createInfo.sType = VK_STRUCTURE_TYPE_DEVICE_CREATE_INFO;

    createInfo.queueCreateInfoCount = static_cast<uint32_t>(queueCreateInfos.size());
    createInfo.pQueueCreateInfos = queueCreateInfos.data();

    createInfo.pEnabledFeatures = &deviceFeatures;

    createInfo.enabledExtensionCount = static_cast<uint32_t>(deviceExtensions.size());
    createInfo.ppEnabledExtensionNames = deviceExtensions.data();

    if(enableValidationLayers) {
        createInfo.enabledLayerCount = static_cast<uint32_t>(validationLayers.size());
        createInfo.ppEnabledLayerNames = validationLayers.data();
    } else {
        createInfo.enabledLayerCount = 0;
    }

    if(vkCreateDevice(physicalDevice, &createInfo, nullptr, &device) not_eq VK_SUCCESS) {
        rendererLogger->error("Failed to create logical device");
        throw std::runtime_error("Failed to create logical device!");
    } else {
        vkGetDeviceQueue(device, indices.graphicsFamily.value(), 0, &graphicsQueue);
        vkGetDeviceQueue(device, indices.presentFamily.value(), 0, &presentQueue);
    }
}

bool Renderer::checkValidationLayerSupport() {
    uint32_t layerCount;
    vkEnumerateInstanceLayerProperties(&layerCount, nullptr);

    std::vector<VkLayerProperties> availableLayers{layerCount};
    vkEnumerateInstanceLayerProperties(&layerCount, availableLayers.data());

    for(char const *layerName : validationLayers) {
        bool layerFound = false;

        for(auto const &layerProperties : availableLayers) {
            if(std::strcmp(layerName, layerProperties.layerName) == 0) {
                layerFound = true;
                rendererLogger->debug("Found layer: {}", layerProperties.layerName); 
                break;
            }
        }

        if(not layerFound) {
            rendererLogger->warn("Couldn't find layer: {}", layerName);
            return false;
        }
    }

    return true;
}

void Renderer::setupDebugMessenger() {
    if(not enableValidationLayers) return;

    VkDebugUtilsMessengerCreateInfoEXT createInfo;
    populateDebugMessengerCreateInfo(createInfo);

    if(createDebugUtilsMessengerEXT(instance, &createInfo, nullptr, &debugMessenger) not_eq VK_SUCCESS) {
        rendererLogger->error("Failed to set up debug messenger!");
        throw std::runtime_error("Failed to set up debug messenger!");
    }
}

void Renderer::createSurface(GLFWwindow *window) {
    if (glfwCreateWindowSurface(instance, window, nullptr, &surface) not_eq VK_SUCCESS) {
        rendererLogger->error("Failed to create window surface");
        throw std::runtime_error("Failed to create window surface!");
    }
}

void Renderer::populateDebugMessengerCreateInfo(VkDebugUtilsMessengerCreateInfoEXT &createInfo) {
    createInfo = {};
    createInfo.sType = VK_STRUCTURE_TYPE_DEBUG_UTILS_MESSENGER_CREATE_INFO_EXT;
    if(vulkanDebugPedantic) {
        createInfo.messageSeverity = VK_DEBUG_UTILS_MESSAGE_SEVERITY_VERBOSE_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_SEVERITY_WARNING_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_SEVERITY_ERROR_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_SEVERITY_INFO_BIT_EXT;
    } else {
        createInfo.messageSeverity = VK_DEBUG_UTILS_MESSAGE_SEVERITY_VERBOSE_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_SEVERITY_WARNING_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_SEVERITY_ERROR_BIT_EXT;
    }
    createInfo.messageType = VK_DEBUG_UTILS_MESSAGE_TYPE_GENERAL_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_TYPE_VALIDATION_BIT_EXT bitor VK_DEBUG_UTILS_MESSAGE_TYPE_PERFORMANCE_BIT_EXT;
    createInfo.pUserData = this->rendererLogger.get();
    createInfo.pfnUserCallback = debugCallback;
}

std::vector<char const*> Renderer::getRequiredExtensions() {
    uint32_t extensionCount = 0;
    vkEnumerateInstanceExtensionProperties(nullptr, &extensionCount, nullptr);

    std::vector<VkExtensionProperties> instanceExtensionsProperties(extensionCount);
    vkEnumerateInstanceExtensionProperties(nullptr, &extensionCount, instanceExtensionsProperties.data());

    for (const auto& extension : instanceExtensionsProperties) {
        rendererLogger->debug("Found instance extension: {}", extension.extensionName);
    }

    uint32_t glfwExtensionCount = 0;
    char const** glfwExtensions;

    glfwExtensions = glfwGetRequiredInstanceExtensions(&glfwExtensionCount);
    std::vector<char const*> extensions(glfwExtensions, glfwExtensions + glfwExtensionCount);

    if(enableValidationLayers) {
        extensions.push_back(VK_EXT_DEBUG_UTILS_EXTENSION_NAME);
        rendererLogger->debug("Added instance extension: {}", VK_EXT_DEBUG_UTILS_EXTENSION_NAME);
    }

    for(auto const *extension : instanceExtensions) {
        extensions.push_back(extension);
        rendererLogger->debug("Added instance extension: {}", extension);
    }
    return extensions;
}


